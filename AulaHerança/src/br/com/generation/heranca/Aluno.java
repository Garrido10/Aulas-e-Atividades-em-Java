package br.com.generation.heranca;

public class Aluno {
		
		
		// Atributos
	
		String semestre;
		String curso;
		
	
		// Get/Set == Métodos que servem para acesso e mudanças com relação aos Atributos
		// Get - Acesso/Recuperação .. Set - Mudança/Modificação
		
		
		public String getSemestre() {
			return semestre;
		}
		public void setSemestre(String semestre) {
			this.semestre = semestre;
		}
		public String getCurso() {
			return curso;
		}
		public void setCurso(String curso) {
			this.curso = curso;
		} 
		
		
}
