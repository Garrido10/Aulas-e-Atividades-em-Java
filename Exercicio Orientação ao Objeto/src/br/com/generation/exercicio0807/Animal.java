package br.com.generation.exercicio0807;

public class Animal {
	
		//Atributos
	
		private String nome; 
		private String tipo; 
		private int idade;
	
		
		
		public String getNome() {
			return nome;
		}
		public void setNome(String nome) {
			this.nome = nome;
		}
		public String getTipo() {
			return tipo;
		}
		public void setTipo(String tipo) {
			this.tipo = tipo;
		}
		public int getIdade() {
			return idade;
		}
		public void setIdade(int idade) {
			this.idade = idade;
		} 
		
		

}
